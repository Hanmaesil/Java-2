
public class Number {

	public static void main(String[] args) {

		//operater 연산자
		
		System.out.println(15+22); // 37
		System.out.println(33-21); // 12
		System.out.println(33*21); // 693
		System.out.println(33/2); // 16
		System.out.println(Math.ceil(33/2));
		
		System.out.println(Math.PI); // 3.14
		System.out.println(Math.floor(Math.PI)); // 소수점 제거
		System.out.println(Math.ceil(Math.PI)); // 소수점 올리기
		
	}

}
